int main() {
    Solution s;
    vector<int> data{3, 4, 5, 1, 2};
    cout << "1 ?= " << s.findMin(data) << endl;
    vector<int> data2{4, 5, 6, 7, 0, 1, 2};
    cout << "0 ?= " << s.findMin(data2) << endl;
}