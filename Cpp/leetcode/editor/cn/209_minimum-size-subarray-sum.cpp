    int minSubArrayLen(int target, vector<int>& nums) {
        int len = nums.size();
        int min = INT32_MAX;
        for (int i = 0; i < len; i++) {
            int sum = 0;
            for (int j = i; j < len; j++) {
                sum += nums[j];
                if (sum >= target) {
                    min = std::min(min, j - i + 1);
                    break;
                }
            }
        }

        return min == INT32_MAX ? 0 : min;
    }

    // BF time: O(n^2) space: O(1)
